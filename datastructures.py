score = int(input("Enter your score:"))

if score >= 80:
    print("Grade: A") 
    print("excellent!")
elif score >= 70:
    print("Grade: B")
    print("good") 
elif score >= 60:
    print("Grade: C")
    print("fair") 
elif score >= 50:
    print("Grade: D")
    print("poor") 
else:
    print("Grade: F")
    print("fail") 

 